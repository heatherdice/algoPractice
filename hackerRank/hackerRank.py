# Simple Array Sum
# Given an arr of ints, find sum of its elements
# Ex: [1,2,3] -> 1+2+3=6 -> return 6
def simpleArraySum(arr):
    sum = 0
    for i in range(len(arr)):
        sum += arr[i]
    return sum

